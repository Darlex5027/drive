public class Clase1{
	private int edad;
	private String nombre;



	//Setter
	public void setNombre(String _nombre){
		this.nombre=_nombre;
	}
	public void setEdad(int _edad){
		this.edad=_edad;
	}
	//Getter
	public String getNombre(){
		return this.nombre;
	}
	public int getEdad(){
		return this.edad;
	}
}
